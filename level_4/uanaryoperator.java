public class uanaryoperator {
    public static void main(String[] args) {
        int x=5;
        int y=-x;
        int z=-y;
        System.out.println(z);
        int p=5;
        System.out.println(p++);
        System.out.println(p);
        System.out.println(++p);
        System.out.println(p);
    }
}
